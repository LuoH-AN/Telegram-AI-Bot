import re as _re

# Variant suffixes lobehub/ts data appends; stripping them often yields the base
# id users actually configure (e.g. "qwen-max-latest" -> "qwen-max").
_VARIANT_SUFFIX = _re.compile(
    r"(?:-(?:latest|preview|exp|turbo|vision|thinking|reasoning))"
    r"(?:-\d{2,4}-\d{2}-\d{2})?$"
    r"|-\d{3,}$"          # -001 / -002 / -8192 style numeric tails
    r"|-(?:20\d{2}[a-z]*-\d{2}-\d{2})$",  # dated releases
)


def get_model_context_limit(model_id: str) -> int | None:
    """Resolve a model id to its context-window token limit, or None if unknown.

    Tries exact match, then strips common lobehub variant suffixes and retries,
    so configuring ``gpt-4o`` / ``qwen-max`` (base) still resolves even when the
    table only has ``qwen-max-latest``.
    """
    if not model_id:
        return None
    mid = model_id.strip()
    direct = MODEL_CONTEXT_LIMITS.get(mid)
    if direct is not None:
        return direct
    # Variant suffix stripping (iterative, e.g. -preview-2024-01-01)
    candidate = mid
    for _ in range(3):
        stripped = _VARIANT_SUFFIX.sub("", candidate)
        if stripped == candidate:
            break
        candidate = stripped
        if candidate in MODEL_CONTEXT_LIMITS:
            return MODEL_CONTEXT_LIMITS[candidate]
    # Last resort: any table key whose base (pre-variant) form equals ours
    return None


def format_context_window_note(model_id: str) -> str:
    """One-line context-window hint for UI, or '' if the model is unknown."""
    limit = get_model_context_limit(model_id)
    if not limit:
        return ""
    return f"🪟 {limit:,} context tokens"
